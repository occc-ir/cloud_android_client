package ir.occc.android.feedback;

import java.net.URL;

public class FeedbackData {
	private URL URL;
	private String Data;
	
	public URL getURL() {
		return URL;
	}
	
	public void setURL(URL uRL) {
		URL = uRL;
	}
	
	public String getData() {
		return Data;
	}
	
	public void setData(String data) {
		Data = data;
	}
}
